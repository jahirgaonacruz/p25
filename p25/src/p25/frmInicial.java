    private void btnConectarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConectarActionPerformed
        String usr = txtUsuario.getText();
        String pwd = txtPassword.getText();
        String base = txtBase.getText();
        
        if (usr.isEmpty() || base.isEmpty()){
            JOptionPane.showMessageDialog(null,"Debes escribir los datos que se piden.");
        }else{
            try{
                cnn.obtener(usr, pwd, base);
                JOptionPane.showMessageDialog(null,"Conexion exitosa!");
            } catch (SQLException | ClassNotFoundException ex){
                JOptionPane.showMessageDialog(null,"Error el conectarse a la base de datos.");
            }finally{
            txtUsuario.setText("");
            txtPassword.setText("");
            txtBase.setText("");
        }
        }
    }//GEN-LAST:event_btnConectarActionPerformed

    private void btnDesconectarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDesconectarActionPerformed
        try{
            cnn.cerrar();
            JOptionPane.showMessageDialog(null, "Desconexi칩n correcta!");
        }  catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Desconexi칩n incorrecta!");
        }
    }//GEN-LAST:event_btnDesconectarActionPerformed

    private void btnSallirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSallirActionPerformed
        try {
            JOptionPane.showMessageDialog(null, "Cerrarndo la base de datos...");
            TimeUnit.SECONDS.sleep(2);
            cnn.cerrar();
            JOptionPane.showMessageDialog(null, "Cierre correcto!");
            System.exit(0);
        } catch (SQLException | InterruptedException ex) {
            JOptionPane.showMessageDialog(null, "Lo sentimos , se produjo un error al cerrar la base de datos :(");
        }
    }//GEN-LAST:event_btnSallirActionPerformed

